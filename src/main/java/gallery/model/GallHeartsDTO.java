package gallery.model;

public class GallHeartsDTO{

	private String heartno;
	private String gallno;
	private String heart;
	
	public String getHeartno() {
		return heartno;
	}
	public void setHeartno(String heartno) {
		this.heartno = heartno;
	}
	public String getGallno() {
		return gallno;
	}
	public void setGallno(String gallno) {
		this.gallno = gallno;
	}
	public String getHeart() {
		return heart;
	}
	public void setHeart(String heart) {
		this.heart = heart;
	}
}
