package freeboard.model;

public class FreeHeartsDTO{

	private String heartno;
	private String freeno;
	private String heart;
	
	public String getHeartno() {
		return heartno;
	}
	public void setHeartno(String heartno) {
		this.heartno = heartno;
	}
	public String getFreeno() {
		return freeno;
	}
	public void setFreeno(String freeno) {
		this.freeno = freeno;
	}
	public String getHeart() {
		return heart;
	}
	public void setHeart(String heart) {
		this.heart = heart;
	}
}
